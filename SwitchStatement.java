package com.sample;
public class SwitchStatement {
public static void main(String[] args) {
int percent = 90;
switch (percent) {
case 90:
System.out.println("Grade A");
break;
case 80:
System.out.println("Grade B");
break;
case 70:
System.out.println("Grade C");
break;
default:
System.out.println("not mentioned in above Case");
break;
}
}
}
